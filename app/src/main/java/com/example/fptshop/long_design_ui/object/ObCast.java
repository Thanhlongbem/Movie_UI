package com.example.fptshop.long_design_ui.object;

public class ObCast {
    private int ImageCast;
    private String NameCast;

    public ObCast(int imageCast, String nameCast) {
        ImageCast = imageCast;
        NameCast = nameCast;
    }

    public int getImageCast() {
        return ImageCast;
    }

    public void setImageCast(int imageCast) {
        ImageCast = imageCast;
    }

    public String getNameCast() {
        return NameCast;
    }

    public void setNameCast(String nameCast) {
        NameCast = nameCast;
    }
}
